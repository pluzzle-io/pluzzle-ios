/// Puzzlens — a collection of SwiftUI puzzle-game views for iOS.
///
/// Each game is self-contained and customisable through builder-style modifier chains:
///
/// - ``MinesweeperGameView`` — classic mine-sweeping grid game.
/// - ``KelvinGridView`` — Wordle-style word-guessing game with alphabetical offset hints.
/// - ``WordWheelView`` — word-wheel puzzle where every answer must use the centre letter.
/// - ``StreaksGameView`` — continuous-path drawing puzzle across an N×M grid.
/// - ``SudokuGameView`` — standard 9×9 Sudoku with a built-in number pad.
/// - ``VoxelGameView`` — 3D Tic-Tac-Toe rendered with SceneKit.
